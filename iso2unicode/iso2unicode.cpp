duple * duples = 0;
const int N = 0x2200;
int isotally[17];
int compatibleisocodes = 0;
int allchars = 0;
int alliso = 0;
int allascii = 0;

int makeDuples(void)
    {
    duples = new duple[N];
    int i;
    for(i = 0;i < 17;++i)
        isotally[i] = 0;
    int upos;
    for(upos = 0;upos < 128;++upos)
        {
        duples[upos].eightbit = upos;
        duples[upos].isocodes = 0;
        }
    for(;upos < N;++upos)
        {
        duples[upos].eightbit = 0;
        duples[upos].isocodes = 0;
        }
    int kode;
    for(kode = 0;kode < 15;++kode)
        {
        int isocode = kode < 11 ? kode + 1 : kode + 2;
        int pos;
        int isocodebit = (1 << isocode);
        compatibleisocodes |= isocodebit;
        for(pos = 0;pos < 128;++pos)
            {
            int upos = codes[kode][pos];
            duples[upos].eightbit = pos + 0x0080;
            duples[upos].isocodes |= isocodebit;
            }
        }
    return N;
    }

void deleteDuples(void)
    {
    delete [] duples;
    int i;
    for(i = 0;i < 17;++i)
        isotally[i] = 0;
    }


int getEightBit(int unicode)
    {
    assert(duples != 0);
    ++allchars;
    if(0x2200 <= unicode)
        {
//        printf("unicode point > 0x2200 found (%x)\n",unicode);
        compatibleisocodes = 0;
        return 0;
        }
    if(unicode > 0xA0)
        {
        int isocodes = duples[unicode].isocodes;
        compatibleisocodes &= isocodes;
        if(isocodes)
            for(int isocode = 1;isocode < 17;++isocode)
                if(isocodes & (1 << isocode))
                    ++isotally[isocode];
        //++isotally[duples[unicode].isocode];
        return duples[unicode].eightbit;
        }
    else
        {
        ++alliso;
        if(unicode < 128)
            ++allascii;
        return unicode;
        }
    }

char * report()
    {
    static char buf[1000];
    char * pbuf = buf;
    int i;
    int max = 0;
    for(i = 0;i < 17;++i)
        if(isotally[i] > max)
            max = isotally[i];
    printf("All characters:         %10d\nDetails\nAll characters >= 0xA0: %10d\nAll ASCII characters:   %10d\n0x80 < 0xA0:            %10d\n\n",allchars,allchars - alliso,allascii,alliso - allascii);
    if(compatibleisocodes)
        {
        printf("ISO-8859 encodings that capture all characters:\n");
        for(i = 0;i < 17;++i)
            {
            if(compatibleisocodes & (1 << i))
                {
                printf("ISO-8859-%d\n",i);
                }
            }
        }
    else
        {
        printf("No ISO-8859 encoding exist that captures all characters.\n");
        }
    printf("\n");
    while(max)
        {
        int newmax = 0;
        int L;
        printf("Capturing %d of %d (%f%%):\n",max,allchars - alliso,100.0*(double)max/(double)(allchars - alliso));
        for(i = 0;i < 17;++i)
            {
            if(isotally[i] == max)
                {
                //L = sprintf(pbuf,"%d\tISO-8859-%d %s\n",isotally[i],i,ISO_8859_names[i]);
                L = printf("ISO-8859-%d\t%s\n",i,ISO_8859_names[i]);
                pbuf += L;
                }
            else if(max > isotally[i] && isotally[i] > newmax)
                newmax = isotally[i];
            }
        //L = sprintf(pbuf,"\n");
        L = printf("\n");
        max = newmax;
        }
    return buf;
    }
