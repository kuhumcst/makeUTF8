int * getCode(int isocode)
    {
    if(0 < isocode && isocode < 12)
        return codes[isocode - 1];
    else if(12 < isocode && isocode <= 16)
        return codes[isocode - 2];
    else if(850 == isocode)
        return codes[15];
    else
        return 0;
    }

